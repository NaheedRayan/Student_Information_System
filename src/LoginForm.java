    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        //When login button is clicked
        
        try {
            
            
            if(txtUser.getText().isEmpty() ||txtPass.getText().isEmpty() )
            {
                JOptionPane.showMessageDialog(null, "Fields cannot be empty");
            }
            else 
            {
                String username = txtUser.getText() ;
                String password = txtPass.getText() ;
                
                
                Class.forName("com.mysql.jdbc.Driver") ;
                con = DriverManager.getConnection("jdbc:mysql://localhost/javacontactsapp" ,"root" ,"");
                pst = con.prepareStatement("select * from user where username = ? and pass = ? ") ;
                
                pst.setString(1, username);
                pst.setString(2, password);
                rs = pst.executeQuery();
                
                if(rs.next())//if the password is correct
                {
                   New s = new New() ;
                   this.hide();
                   s.setLocationRelativeTo(null);//setting the location
                   s.setVisible(true);//showing new dialog box
                   s.pack();//making it dynamic in size
                   
                   
                    //this.dispose();//for closing the loginForm
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Username or Password do not match");
                    txtUser.setText("");
                    txtPass.setText("");//for making them blank
                    txtUser.requestFocus();
                    
                    
                }
                
                
            }
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
              
        
        
    }//GEN-LAST:event_jButtonLoginActionPerformed

